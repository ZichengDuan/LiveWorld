def _estimate_nav_radius_screen_uniform(
    *,
    pts_local: np.ndarray,
    intrinsics: np.ndarray,
    target_w: Optional[int],
    target_shift_ratio: float,
    screen_depth_percentile: float,
) -> Optional[float]:
    """Estimate nav radius from desired screen-space displacement."""
    if pts_local is None or len(pts_local) == 0:
        return None
    if intrinsics is None or np.asarray(intrinsics).shape != (3, 3):
        return None

    z = np.asarray(pts_local[:, 2], dtype=np.float32)
    valid = np.isfinite(z) & (z > 0.25)
    if valid.sum() < 128:
        return None

    z_ref = float(np.percentile(z[valid], float(np.clip(screen_depth_percentile, 5.0, 95.0))))
    fx = float(abs(intrinsics[0, 0]))
    if not np.isfinite(fx) or fx < 1e-6:
        return None

    if target_w is None or int(target_w) <= 0:
        return None

    ratio = float(np.clip(target_shift_ratio, 0.05, 0.45))
    target_px = float(target_w) * ratio
    d = target_px * z_ref / fx
    nav_radius = d / 0.8
    return float(np.clip(nav_radius, 0.4, 3.2))


def estimate_scene_scale(
    det: ObjectDetection,
    scene_features: SceneFeatures,
    image_path: str,
    geometry_path: str,
    case_type: str,
    llm_motion_scaling: bool,
    qwen_model_path: str,
    scene_meta: Optional[Dict] = None,
    distance_mode: str = "screen_uniform",
    target_shift_ratio: float = 0.18,
    screen_depth_percentile: float = 45.0,
    distance_blend_alpha: float = 0.7,
) -> SceneScale:
    """Estimate nav_radius / event_radius for trajectory planning (strict screen_uniform)."""
    _ = geometry_path
    _ = distance_blend_alpha
    if distance_mode != "screen_uniform":
        raise ValueError(
            f"Unsupported distance_mode={distance_mode}. Only 'screen_uniform' is allowed."
        )
    if scene_meta is None:
        raise ValueError("scene_meta is required for strict trajectory scale estimation.")

    motion_scale = _estimate_motion_scale_from_area_ratio(det.area_ratio)
    motion_scale = float(motion_scale * (1.1 - 0.35 * scene_features.clutter_score))
    llm_scale = _maybe_llm_adjust_motion_scale(
        enable=llm_motion_scaling,
        image_path=image_path,
        qwen_model_path=qwen_model_path,
        area_ratio=det.area_ratio,
        scene_features=scene_features,
        case_type=case_type,
    )
    if llm_scale is not None:
        motion_scale = 0.6 * motion_scale + 0.4 * llm_scale

    c2w = scene_meta["c2w"]  # (4,4)
    w2c = np.linalg.inv(c2w)
    pts_w = scene_meta["points_world"]
    ones = np.ones((len(pts_w), 1), dtype=np.float32)
    pts_local = (w2c @ np.hstack([pts_w, ones]).T).T[:, :3]
    extent_x = float(np.percentile(pts_local[:, 0], 95) - np.percentile(pts_local[:, 0], 5))
    extent_z = float(np.percentile(pts_local[:, 2], 95) - np.percentile(pts_local[:, 2], 5))
    nav_radius = _estimate_nav_radius_screen_uniform(
        pts_local=pts_local,
        intrinsics=scene_meta.get("intrinsics"),
        target_w=scene_meta.get("target_w"),
        target_shift_ratio=target_shift_ratio,
        screen_depth_percentile=screen_depth_percentile,
    )
    if nav_radius is None:
        raise ValueError("Failed to estimate nav_radius in strict screen_uniform mode.")

    event_radius = float(np.clip(2.0 * nav_radius, 0.8, 6.0))
    return SceneScale(
        scene_center=[0.0, 0.0, 0.0],
        scene_extent_x=float(max(0.1, extent_x)),
        scene_extent_z=float(max(0.1, extent_z)),
        nav_radius=nav_radius,
        event_radius=event_radius,
        motion_scale=float(motion_scale),
        geometry_used=True,
    )


