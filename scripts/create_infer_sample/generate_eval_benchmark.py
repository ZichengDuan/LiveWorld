def _circle_points_local(
    radius: float,
    count: int,
    y: float = 0.0,
    z_offset: float = 0.0,
    angle_offset_deg: float = 0.0,
) -> List[List[float]]:
    """Circle in the local XZ plane centred at origin."""
    pts: List[List[float]] = []
    for i in range(count):
        ang = math.radians(angle_offset_deg + (360.0 * i / count))
        x = radius * math.sin(ang)
        z = z_offset + radius * math.cos(ang)
        pts.append([float(x), float(y), float(z)])
    return pts


def _normalize(v: np.ndarray) -> np.ndarray:
    n = float(np.linalg.norm(v))
    if n < 1e-8:
        return v
    return v / n


def _lookat_local(position: np.ndarray, look_at: np.ndarray) -> np.ndarray:
    """Build a 4×4 c2w (local) that looks from *position* towards *look_at*."""
    forward = _normalize(look_at - position)
    if float(np.linalg.norm(forward)) < 1e-8:
        forward = np.array([0.0, 0.0, 1.0], dtype=np.float32)
    world_up = np.array([0.0, -1.0, 0.0], dtype=np.float32)
    right = np.cross(forward, world_up)
    if float(np.linalg.norm(right)) < 1e-8:
        world_up = np.array([1.0, 0.0, 0.0], dtype=np.float32)
        right = np.cross(forward, world_up)
    right = _normalize(right)
    down = _normalize(np.cross(forward, right))

    c2w = np.eye(4, dtype=np.float32)
    c2w[:3, 0] = right
    c2w[:3, 1] = down
    c2w[:3, 2] = forward
    c2w[:3, 3] = position
    return c2w


def _local_to_world(local_c2w: np.ndarray, first_c2w: np.ndarray) -> np.ndarray:
    """Transform a local-frame c2w into world coordinates."""
    return (first_c2w @ local_c2w).astype(np.float32)


