def _is_static_entity_label(text: str) -> bool:
    lowered = str(text).strip().lower()
    if not lowered:
        return True
    if lowered in {"none", "nothing", "background"}:
        return True
    for kw in _STATIC_ENTITY_KEYWORDS:
        if re.search(rf"\b{re.escape(kw)}\b", lowered):
            return True
    return False


def _bbox_from_mask(mask: np.ndarray) -> Tuple[int, int, int, int]:
    ys, xs = np.where(mask > 0)
    if len(xs) == 0:
        return (0, 0, 0, 0)
    return (int(xs.min()), int(ys.min()), int(xs.max()), int(ys.max()))


def detect_event_object(
    image_path: str,
    qwen_model_path: str,
    sam3_model_path: str,
    scene_detect_prompt: str,
    qwen_device: str = "cuda",
    cpu_offload_qwen: bool = False,
    cpu_offload_sam3: bool = False,
) -> Tuple[ObjectDetection, np.ndarray]:
    """Detect all foreground entities using Qwen+SAM."""
    from scripts.create_train_data._entity_detector import Qwen3VLEntityExtractor
    from scripts.create_train_data._sam3_segmenter import Sam3VideoSegmenter

    qwen_device = str(qwen_device).strip().lower() if qwen_device else "cuda"
    if cpu_offload_qwen:
        qwen_device = "cpu"
    if qwen_device not in {"cuda", "cpu"}:
        qwen_device = "cuda"
    if qwen_device == "cuda":
        try:
            import torch
            if not torch.cuda.is_available():
                qwen_device = "cpu"
        except Exception:
            qwen_device = "cpu"

    cache_key = (qwen_model_path, sam3_model_path, qwen_device, bool(cpu_offload_sam3))
    if cache_key not in _DETECTION_MODEL_CACHE:
        qwen = Qwen3VLEntityExtractor(model_path=qwen_model_path, device=qwen_device)
        sam3 = None if cpu_offload_sam3 else Sam3VideoSegmenter(checkpoint_path=sam3_model_path)
        _DETECTION_MODEL_CACHE[cache_key] = (qwen, sam3)
    else:
        qwen, sam3 = _DETECTION_MODEL_CACHE[cache_key]

    if cpu_offload_sam3 or sam3 is None:
        sam3 = Sam3VideoSegmenter(checkpoint_path=sam3_model_path)

    raw_entities, _ = qwen.extract(image_path, prompt=scene_detect_prompt)
    entities = sorted(
        {
            str(e).strip()
            for e in raw_entities
            if str(e).strip() and not _is_static_entity_label(str(e))
        }
    )
    if entities and hasattr(qwen, "generate_text"):
        try:
            from scripts.create_train_data._entity_detector import parse_entities as _parse_entities

            filter_prompt = _DYNAMIC_FILTER_PROMPT.format(entities=", ".join(entities))
            filter_raw = qwen.generate_text(image_path, filter_prompt)
            filtered = [
                str(e).strip()
                for e in _parse_entities(filter_raw)
                if str(e).strip() and not _is_static_entity_label(str(e))
            ]
            if filtered:
                entities = sorted({e for e in filtered})
        except Exception as exc:
            print(f"  [warn] dynamic entity filtering failed, fallback to raw entities ({exc})")
    image_rgb = _load_image_rgb(image_path)
    h, w = image_rgb.shape[:2]

    if not entities:
        print(
            f"  [warn] Qwen detected no dynamic entities in {image_path}, "
            "using center-crop fallback mask"
        )
        entities = ["foreground entity"]
        union_mask = np.zeros((h, w), dtype=np.uint8)
        cy, cx = h // 2, w // 2
        rh, rw = h // 4, w // 4
        union_mask[cy - rh : cy + rh, cx - rw : cx + rw] = 1
    else:
        union_mask = np.zeros((h, w), dtype=np.uint8)
        for ent in entities:
            seg = sam3.segment(
                video_path=image_path,
                prompts=[ent],
                frame_index=0,
                expected_frames=1,
            )
            if seg is None or seg.size == 0:
                print(f"  [warn] SAM3 returned empty mask for '{ent}', skipping")
                continue
            if seg.ndim == 3:
                mask = seg[0]
            elif seg.ndim == 2:
                mask = seg
            else:
                mask = seg.reshape(seg.shape[-2], seg.shape[-1])
            union_mask = np.maximum(union_mask, (mask > 0).astype(np.uint8))

    x1, y1, x2, y2 = _bbox_from_mask(union_mask)
    if x2 <= x1 or y2 <= y1:
        print(
            f"  [warn] SAM3 union mask for entities {entities} has zero area, "
            "using center-crop fallback mask"
        )
        union_mask = np.zeros((h, w), dtype=np.uint8)
        cy, cx = h // 2, w // 2
        rh, rw = h // 4, w // 4
        union_mask[cy - rh : cy + rh, cx - rw : cx + rw] = 1
        x1, y1, x2, y2 = _bbox_from_mask(union_mask)
    area_ratio = ((x2 - x1 + 1) * (y2 - y1 + 1)) / float(h * w)
    det = ObjectDetection(
        entities=entities,
        bbox_xyxy=(int(x1), int(y1), int(x2), int(y2)),
        area_ratio=float(area_ratio),
        method="qwen_sam",
    )
    if cpu_offload_sam3:
        try:
            import gc
            import torch

            del sam3
            gc.collect()
            if torch.cuda.is_available():
                torch.cuda.empty_cache()
        except Exception:
            pass
    return det, union_mask.astype(np.uint8)


