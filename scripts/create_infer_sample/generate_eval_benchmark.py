def _load_scene_meta(meta_path: str) -> Optional[Dict]:
    """Load scene meta.npz → dict with c2w (4×4) and points_world (N×3)."""
    if not meta_path or not Path(meta_path).exists():
        return None
    data = np.load(meta_path)
    if "c2w" not in data or "points_world" not in data:
        return None
    meta = {
        "c2w": data["c2w"].astype(np.float32),
        "points_world": data["points_world"].astype(np.float32),
        "intrinsics": data["intrinsics"].astype(np.float32),
    }
    if "target_h" in data:
        meta["target_h"] = int(data["target_h"])
    if "target_w" in data:
        meta["target_w"] = int(data["target_w"])
    return meta


def _filter_fg_depth_outliers(
    points_local: np.ndarray,
    u_proj: np.ndarray,
    v_proj: np.ndarray,
    *,
    keep_upper_q: float = 0.90,
) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    """Drop the farthest foreground depth tail to suppress long-tail artifacts."""
    pts = np.asarray(points_local, dtype=np.float32)
    u = np.asarray(u_proj, dtype=np.float32)
    v = np.asarray(v_proj, dtype=np.float32)
    if len(pts) == 0:
        return pts, u, v
    if pts.ndim != 2 or pts.shape[1] != 3:
        raise ValueError(f"points_local must be Nx3, got shape={pts.shape}")
    if len(u) != len(pts) or len(v) != len(pts):
        raise ValueError("u_proj/v_proj must align with points_local length")

    q_hi = float(np.clip(keep_upper_q, 0.5, 1.0))

    z = pts[:, 2]
    finite = np.isfinite(z)
    if not np.any(finite):
        raise ValueError("foreground points have no finite depth values")

    z_finite = z[finite]
    z_hi = float(np.quantile(z_finite, q_hi))
    keep = finite & (z <= z_hi)
    if int(np.count_nonzero(keep)) == 0:
        raise ValueError("depth outlier filtering removed all foreground points")

    return pts[keep], u[keep], v[keep]


def _estimate_event_points_local(
    scene_meta: Optional[Dict],
    fg_union_mask: np.ndarray,
) -> Optional[np.ndarray]:
    """Estimate foreground 3D points in first-frame local camera coordinates."""
    if scene_meta is None:
        return None
    if fg_union_mask is None:
        raise ValueError("fg_union_mask is required for mask-only foreground calibration")
    pts_w = scene_meta.get("points_world")
    c2w = scene_meta.get("c2w")
    K = scene_meta.get("intrinsics")
    if (
        pts_w is None
        or c2w is None
        or K is None
        or np.asarray(K).shape != (3, 3)
        or len(pts_w) == 0
    ):
        return None

    pts_w = np.asarray(pts_w, dtype=np.float32)
    w2c = np.linalg.inv(np.asarray(c2w, dtype=np.float32))
    ones = np.ones((len(pts_w), 1), dtype=np.float32)
    pts_local = (w2c @ np.hstack([pts_w, ones]).T).T[:, :3]

    z = pts_local[:, 2]
    valid = np.isfinite(z) & (z > 0.1)
    if valid.sum() < 64:
        return None

    fx, fy = float(K[0, 0]), float(K[1, 1])
    cx, cy = float(K[0, 2]), float(K[1, 2])
    x = pts_local[:, 0]
    y = pts_local[:, 1]
    u = fx * x / np.maximum(z, 1e-6) + cx
    v = fy * y / np.maximum(z, 1e-6) + cy

    fg_mask = np.asarray(fg_union_mask)
    if fg_mask.ndim != 2:
        raise ValueError(f"fg_union_mask must be HxW, got shape={fg_mask.shape}")
    fg_mask = (fg_mask > 0)
    mask_h, mask_w = fg_mask.shape
    if mask_h <= 0 or mask_w <= 0 or int(np.count_nonzero(fg_mask)) == 0:
        raise ValueError("fg_union_mask is empty")

    target_w = scene_meta.get("target_w")
    target_h = scene_meta.get("target_h")
    if target_w is None or target_h is None:
        raise ValueError("scene_meta must contain target_w/target_h for mask alignment")
    proj_w = max(1, int(target_w))
    proj_h = max(1, int(target_h))

    scale_u = float(mask_w) / float(proj_w)
    scale_v = float(mask_h) / float(proj_h)
    u_m = u * scale_u
    v_m = v * scale_v
    u_i = np.rint(u_m).astype(np.int32)
    v_i = np.rint(v_m).astype(np.int32)

    inside_image = (
        valid
        & np.isfinite(u_m)
        & np.isfinite(v_m)
        & (u_i >= 0)
        & (u_i < int(mask_w))
        & (v_i >= 0)
        & (v_i < int(mask_h))
    )
    if inside_image.sum() < 32:
        return None

    fg_hit = np.zeros_like(inside_image, dtype=bool)
    cand_idx = np.flatnonzero(inside_image)
    if cand_idx.size > 0:
        fg_hit[cand_idx] = fg_mask[v_i[cand_idx], u_i[cand_idx]]
    if fg_hit.sum() < 32:
        return None

    pts = pts_local[fg_hit]
    u_sel = u_m[fg_hit]
    v_sel = v_m[fg_hit]
    pts, u_sel, v_sel = _filter_fg_depth_outliers(pts, u_sel, v_sel)
    if len(pts) < 32:
        return None

    boundary_idx = np.array(
        [
            int(np.argmin(u_sel)),
            int(np.argmax(u_sel)),
            int(np.argmin(v_sel)),
            int(np.argmax(v_sel)),
        ],
        dtype=np.int64,
    )
    boundary_idx = np.unique(boundary_idx)

    if len(pts) > 2048:
        keep_set = set(boundary_idx.tolist())
        budget = 2048 - len(keep_set)
        if budget > 0:
            all_idx = np.arange(len(pts), dtype=np.int64)
            if keep_set:
                mask = np.ones(len(pts), dtype=bool)
                mask[np.array(sorted(keep_set), dtype=np.int64)] = False
                pool = all_idx[mask]
            else:
                pool = all_idx
            if len(pool) <= budget:
                sample_idx = pool
            else:
                sample_idx = pool[np.linspace(0, len(pool) - 1, budget, dtype=np.int64)]
            keep_idx = np.unique(
                np.concatenate(
                    [
                        np.array(sorted(keep_set), dtype=np.int64),
                        np.asarray(sample_idx, dtype=np.int64),
                    ]
                )
            )
        else:
            keep_idx = boundary_idx[:2048]
        pts = pts[keep_idx]

    pts = pts.astype(np.float32, copy=False)
    if not np.all(np.isfinite(pts)):
        pts = pts[np.isfinite(pts).all(axis=1)]
    if len(pts) == 0:
        return None
    return pts


