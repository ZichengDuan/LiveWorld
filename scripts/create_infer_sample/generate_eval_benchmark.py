def _build_location_pool(
    nav_radius: float,
    event_slots: Dict[str, Dict],
) -> Dict[str, List[float]]:
    """Camera locations in local XZ plane."""
    pool: Dict[str, List[float]] = {}
    pool["S"] = [0.0, 0.0, 0.0]
    pool["O"] = [0.0, 0.0, 0.9 * nav_radius]
    return pool


def _build_location_pose_pool(
    location_pool: Dict[str, List[float]],
    event_slots: Dict[str, Dict],
) -> Dict[str, Dict]:
    """For each location, build a local c2w."""
    pose_pool: Dict[str, Dict] = {}
    forward_target = np.array([0.0, 0.0, 1.0], dtype=np.float32)
    for loc, pos_list in location_pool.items():
        pos = np.asarray(pos_list, dtype=np.float32)
        if loc == "S":
            c2w = np.eye(4, dtype=np.float32)
            look = forward_target
        elif loc in event_slots:
            look = np.asarray(event_slots[loc]["position"], dtype=np.float32)
            c2w = _lookat_local(pos, look)
        else:
            look = np.zeros(3, dtype=np.float32)
            c2w = _lookat_local(pos, look)
        pose_pool[loc] = {
            "position": [float(x) for x in pos],
            "look_at": [float(x) for x in look],
            "c2w": c2w.astype(float).tolist(),
        }
    return pose_pool


