def _build_event_slots(
    combo: ComboDef,
    event_points: Dict[str, List[float]],
    entities: List[str],
) -> Dict[str, Dict]:
    """Build event slots based on combo definition."""
    slots: Dict[str, Dict] = {}
    spawn_rounds: Dict[str, int] = {}
    for i, act in enumerate(combo.actions):
        eid = act.get("event", "")
        if act["action"] == "spawn" and eid:
            spawn_rounds[eid] = i
    if combo.first_frame_has_event and "A" in combo.event_ids:
        spawn_rounds.setdefault("A", -1)

    entities_text = ", ".join(entities) if entities else "foreground entity"
    for eid in combo.event_ids:
        if eid not in event_points:
            continue
        slots[eid] = {
            "entities": list(entities),
            "entity": entities_text,
            "position": event_points[eid],
            "spawn_round": spawn_rounds.get(eid, 0),
        }
    return slots


