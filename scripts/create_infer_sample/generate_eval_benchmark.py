def _save_geometry_npz(
    plan: Dict,
    meta_path: str,
    out_dir: Path,
) -> Optional[Path]:
    """Save geometry.npz compatible with infer_spatia.py."""
    if not meta_path or not Path(meta_path).exists():
        return None
    data = np.load(meta_path)
    for key in ("depth_proc", "intrinsics_proc", "processed_size"):
        if key not in data:
            print(f"  [geometry.npz] Skipped: '{key}' not in {meta_path}")
            return None

    depth_proc = data["depth_proc"]
    intrinsics_proc = data["intrinsics_proc"]
    processed_size = data["processed_size"]
    target_h = int(data["target_h"])
    target_w = int(data["target_w"])

    traj = plan["trajectory_frames"]
    total_frames = len(traj)
    poses_c2w = np.array([f["c2w"] for f in traj], dtype=np.float32)

    from liveworld.geometry_utils import scale_intrinsics
    depth_target = cv2.resize(
        depth_proc, (target_w, target_h), interpolation=cv2.INTER_NEAREST,
    )
    intrinsics_target = scale_intrinsics(
        intrinsics_proc,
        (int(processed_size[0]), int(processed_size[1])),
        (target_h, target_w),
    )

    save_dict = {
        "depths": np.stack([depth_proc] * total_frames),
        "poses_c2w": poses_c2w,
        "intrinsics": np.stack([intrinsics_proc] * total_frames),
        "processed_size": processed_size,
        "original_size": np.array([target_h, target_w]),
        "depths_target": depth_target[None],
        "intrinsics_target": intrinsics_target[None],
        "target_size": np.array([target_h, target_w]),
    }
    geom_path = out_dir / "geometry.npz"
    np.savez_compressed(geom_path, **save_dict)
    return geom_path


def generate_plan_for_combo(
    combo: ComboDef,
    args: argparse.Namespace,
    image_rgb: np.ndarray,
    scene_features: SceneFeatures,
    det: ObjectDetection,
    det_union_mask: np.ndarray,
    scene_meta: Optional[Dict],
    first_c2w: np.ndarray,
    scene_scale: SceneScale,
) -> Dict:
    """Generate a single plan for one combo variant."""
    R = scene_scale.nav_radius * combo.layout_scale
    Re = scene_scale.event_radius * combo.layout_scale

    event_points = {eid: [0.0, 0.0, 0.0] for eid in combo.event_ids}
    nav_points = _circle_points_local(R, count=6)

    if det_union_mask is None:
        raise ValueError("det_union_mask is required (mask-only foreground calibration)")
    calibration_points_local = _estimate_event_points_local(scene_meta, det_union_mask)
    calibration_center_local = (
        np.median(calibration_points_local, axis=0).astype(np.float32)
        if calibration_points_local is not None and len(calibration_points_local) > 0
        else None
    )
    calibration_intrinsics = (
        np.asarray(scene_meta.get("intrinsics"), dtype=np.float32)
        if scene_meta is not None and scene_meta.get("intrinsics") is not None
        else None
    )
    calibration_image_width = (
        int(scene_meta.get("target_w"))
        if scene_meta is not None and scene_meta.get("target_w") is not None
        else None
    )
    calibration_image_height = (
        int(scene_meta.get("target_h"))
        if scene_meta is not None and scene_meta.get("target_h") is not None
        else None
    )

    event_points_used = {
        eid: event_points[eid]
        for eid in combo.event_ids
        if eid in event_points
    }

    event_slots = _build_event_slots(combo, event_points_used, det.entities)
    location_pool = _build_location_pool(nav_radius=R, event_slots=event_slots)

    # Add motion-primitive-specific locations
    if combo.motion_primitive.startswith("case1_"):
        d = float(1.6 * R)
        sign = -1.0 if combo.motion_primitive == "case1_left" else 1.0
        location_pool["A"] = [0.0, 0.0, 0.0]
        location_pool["B"] = [float(sign * d), 0.0, 0.0]
    elif combo.motion_primitive.startswith("case2_"):
        d = 1.6 * R
        Z = d / np.sqrt(3.0)
        location_pool["A"] = [0.0, 0.0, 0.0]
        if combo.motion_primitive == "case2_right":
            location_pool["B"] = [float(d), 0.0, float(-Z)]
        else:
            location_pool["B"] = [float(-d), 0.0, float(-Z)]
        location_pool["C"] = [0.0, 0.0, float(-Z)]

    location_pose_pool = _build_location_pose_pool(
        location_pool=location_pool, event_slots=event_slots,
    )
    round_plan = _build_round_plan(
        combo=combo,
        location_pool=location_pool,
        location_pose_pool=location_pose_pool,
        event_slots=event_slots,
    )
    fpr = combo.frames_per_round if combo.frames_per_round is not None else args.frames_per_round
    trajectory_frames = _interpolate_pose_sequence(
        round_plan=round_plan,
        location_pose_pool=location_pose_pool,
        frames_per_round=fpr,
        first_c2w=first_c2w,
        motion_primitive=combo.motion_primitive,
        nav_radius=R,
        seed=args.seed,
        calibration_points_local=calibration_points_local,
        calibration_intrinsics=calibration_intrinsics,
        calibration_image_width=calibration_image_width,
        calibration_image_height=calibration_image_height,
        exit_visible_ratio=float(getattr(args, "exit_visible_ratio", 0.0)),
    )
    iter_input = _build_iter_input(combo.case_type, round_plan, event_slots)

    # Convert event slot positions local → world
    event_slots_world = {}
    for eid, info in event_slots.items():
        p_local = np.array(info["position"] + [1.0], dtype=np.float32)
        p_world = (first_c2w @ p_local)[:3]
        event_slots_world[eid] = {
            **info,
            "position": [float(p_world[0]), float(p_world[1]), float(p_world[2])],
            "position_local": info["position"],
        }

    # Convert location pool local → world
    location_pool_world = {}
    for loc, pos in location_pool.items():
        p_local = np.array(pos + [1.0], dtype=np.float32)
        p_world = (first_c2w @ p_local)[:3]
        location_pool_world[loc] = [
            float(p_world[0]), float(p_world[1]), float(p_world[2]),
        ]

    sample_id = (
        args.sample_id
        or f"{Path(args.image_path).stem}_{combo.combo_id}_seed{args.seed}"
    )

    exit_visible_ratio = float(getattr(args, "exit_visible_ratio", 0.0))
    exit_visible_threshold = _compute_exit_visible_threshold(
        calibration_points_local,
        exit_visible_ratio,
    )
    if exit_visible_ratio <= 0.0:
        exit_criterion = "visible_fg_projection_count == 0"
    else:
        exit_criterion = (
            f"visible_fg_projection_count <= {exit_visible_threshold} "
            f"(ratio <= {exit_visible_ratio:.4f})"
        )

    output = {
        "sample_id": sample_id,
        "image_path": args.image_path,
        "scene_meta_path": getattr(args, "scene_meta_path", ""),
        "geometry_path": getattr(args, "geometry_path", ""),
        "combo_id": combo.combo_id,
        "case_type": combo.case_type,
        "motion_primitive": combo.motion_primitive,
        "num_rounds": combo.num_rounds,
        "num_events": combo.num_events,
        "first_frame_has_event": combo.first_frame_has_event,
        "first_c2w": first_c2w.astype(float).tolist(),
        "object_detection": asdict(det),
        "scene_features": asdict(scene_features),
        "scene_scale": asdict(scene_scale),
        "distance_normalization": {
            "mode": str(args.distance_mode),
            "target_shift_ratio": float(args.target_shift_ratio),
            "screen_depth_percentile": float(args.screen_depth_percentile),
            "distance_blend_alpha": float(args.distance_blend_alpha),
        },
        "view_calibration": {
            "enabled": bool(calibration_points_local is not None and calibration_intrinsics is not None),
            "event_center_local": (
                [float(x) for x in calibration_center_local.tolist()]
                if calibration_center_local is not None else None
            ),
            "calibration_points_source": "foreground_event_points",
            "calibration_points_count": (
                int(len(calibration_points_local))
                if calibration_points_local is not None
                else 0
            ),
            "exit_visible_ratio": float(exit_visible_ratio),
            "exit_visible_count_threshold": int(exit_visible_threshold),
            "all_foreground_points_exit_required": bool(exit_visible_ratio <= 0.0),
            "projection_exit_criterion": exit_criterion,
            "image_width": int(calibration_image_width) if calibration_image_width is not None else None,
            "image_height": int(calibration_image_height) if calibration_image_height is not None else None,
        },
        "event_slots": event_slots_world,
        "location_pool": location_pool_world,
        "round_target_sequence": combo.target_sequence,
        "nav_points": {f"N{i}": p for i, p in enumerate(nav_points)},
        "round_plan": round_plan,
        "frames_per_round": int(fpr),
        "fps": int(combo.fps),
        "total_frames": int(len(trajectory_frames)),
        "trajectory_frames": trajectory_frames,
        "iter_input": iter_input,
        "camera_control": {
            "yaw_rate_limit_deg_per_frame": float(args.yaw_rate_limit_deg_per_frame),
            "pitch_limit_deg": [-15.0, 15.0],
            "alpha_event": float(args.alpha_event),
            "alpha_non_event": float(args.alpha_non_event),
        },
        "quality_gate": {
            "min_in_frame_ratio": float(args.min_in_frame_ratio),
            "min_event_separation_deg": float(args.min_event_separation_deg),
        },
    }

    out_root = Path(args.output_dir) / sample_id
    out_root.mkdir(parents=True, exist_ok=True)
    with (out_root / "plan.json").open("w", encoding="utf-8") as f:
        json.dump(output, f, ensure_ascii=False, indent=2)

    meta_path = getattr(args, "scene_meta_path", "")
    geom_path = _save_geometry_npz(output, meta_path, out_root)
    if geom_path is not None:
        print(f"  [event_bench] geometry.npz saved: {geom_path}")

    return output


def _load_scene_detect_prompt(system_config_path: str) -> str:
    """Load scene_detect_prompt from system_config.yaml."""
    with open(system_config_path, "r", encoding="utf-8") as f:
        cfg = yaml.safe_load(f)
    return cfg["event"]["detection"]["scene_detect_prompt"]


def generate_all_combos(args: argparse.Namespace) -> List[Dict]:
    """Generate plans for all combos of the specified case_type."""
    scene_detect_prompt = _load_scene_detect_prompt(args.system_config_path)
    image_rgb = _load_image_rgb(args.image_path)
    scene_features = extract_scene_features(image_rgb)
    scene_meta = _load_scene_meta(getattr(args, "scene_meta_path", ""))
    detect_image_path = _prepare_detection_image_for_scene(args.image_path, scene_meta)
    det, det_union_mask = detect_event_object(
        image_path=detect_image_path,
        qwen_model_path=args.qwen_model_path,
        sam3_model_path=args.sam3_model_path,
        scene_detect_prompt=scene_detect_prompt,
    )
    first_c2w = (
        scene_meta["c2w"] if scene_meta is not None
        else np.eye(4, dtype=np.float32)
    )
    scene_scale = estimate_scene_scale(
        det=det,
        scene_features=scene_features,
        image_path=args.image_path,
        geometry_path=getattr(args, "geometry_path", ""),
        case_type=args.case_type,
        llm_motion_scaling=args.llm_motion_scaling,
        qwen_model_path=args.qwen_model_path,
        scene_meta=scene_meta,
        distance_mode=args.distance_mode,
        target_shift_ratio=args.target_shift_ratio,
        screen_depth_percentile=args.screen_depth_percentile,
        distance_blend_alpha=args.distance_blend_alpha,
    )

    combos = get_combos_for_case(
        args.case_type,
        all_variants=getattr(args, "all_variants", False),
    )
    plans = []
    for combo in combos:
        plan = generate_plan_for_combo(
            combo=combo,
            args=args,
            image_rgb=image_rgb,
            scene_features=scene_features,
            det=det,
            det_union_mask=det_union_mask,
            scene_meta=scene_meta,
            first_c2w=first_c2w,
            scene_scale=scene_scale,
        )
        plans.append(plan)
        print(f"  [event_bench] {combo.combo_id}: {plan['sample_id']}")
    return plans


