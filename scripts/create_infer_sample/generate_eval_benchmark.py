def _build_iter_input(
    case_type: str,
    round_plan: Dict[str, Dict],
    event_slots: Dict[str, Dict],
) -> Dict[str, Dict[str, str]]:
    out: Dict[str, Dict[str, str]] = {}
    keys = sorted(round_plan.keys(), key=lambda x: int(x))
    for k in keys:
        r = round_plan[k]
        action = r["action"]
        event_id = r["event"]
        scene_text = "A consistent indoor scene with smooth camera motion."
        fg_text = ""
        if event_id and event_id in event_slots:
            entity = event_slots[event_id]["entity"]
            if action == "spawn":
                fg_text = f"{entity} starts an event naturally at its fixed location."
            elif action == "revisit":
                fg_text = f"{entity} continues naturally at the same location."
            else:
                fg_text = f"{entity} remains in its local area."
        out[str(k)] = {"scene_text": scene_text, "fg_text": fg_text}
    return out


