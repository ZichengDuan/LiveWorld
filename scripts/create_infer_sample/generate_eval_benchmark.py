def _yaw_c2w_local(yaw_deg: float, position: Optional[np.ndarray] = None) -> np.ndarray:
    """Build a local c2w with given yaw angle around Y-down axis."""
    rad = math.radians(yaw_deg)
    c = math.cos(rad)
    s = math.sin(rad)
    R = np.array([
        [ c, 0.0, s],
        [0.0, 1.0, 0.0],
        [-s, 0.0, c],
    ], dtype=np.float32)
    c2w = np.eye(4, dtype=np.float32)
    c2w[:3, :3] = R
    if position is not None:
        c2w[:3, 3] = position
    return c2w


def _count_visible_fg_projection_points(
    *,
    points_local: np.ndarray,
    intrinsics: np.ndarray,
    image_width: int,
    image_height: int,
    position_local: np.ndarray,
    yaw_deg: float,
) -> int:
    """Count visible foreground projections for one candidate local pose."""
    pts = np.asarray(points_local, dtype=np.float32)
    if pts.ndim != 2 or pts.shape[1] != 3 or len(pts) == 0:
        return 0

    c2w = _yaw_c2w_local(float(yaw_deg), position=np.asarray(position_local, dtype=np.float32))
    R = c2w[:3, :3]
    t = c2w[:3, 3]
    p_cam = (R.T @ (pts - t).T).T

    z = p_cam[:, 2]
    valid_z = np.isfinite(z) & (z > 1e-6)
    if not np.any(valid_z):
        return 0

    fx = float(intrinsics[0, 0])
    fy = float(intrinsics[1, 1])
    cx = float(intrinsics[0, 2])
    cy = float(intrinsics[1, 2])
    u = fx * p_cam[:, 0] / np.maximum(z, 1e-6) + cx
    v = fy * p_cam[:, 1] / np.maximum(z, 1e-6) + cy

    W = max(1, int(image_width))
    H = max(1, int(image_height))
    visible = (
        valid_z
        & np.isfinite(u)
        & np.isfinite(v)
        & (u >= 0.0)
        & (u < float(W))
        & (v >= 0.0)
        & (v < float(H))
    )
    return int(np.count_nonzero(visible))


def _compute_exit_visible_threshold(
    points_local: Optional[np.ndarray],
    exit_visible_ratio: float,
) -> int:
    """Compute visible-count threshold for exit detection."""
    if exit_visible_ratio <= 0:
        return 0
    if points_local is None:
        return 0
    total = int(len(points_local))
    if total <= 0:
        return 0
    ratio = float(np.clip(exit_visible_ratio, 0.0, 1.0))
    if ratio <= 0.0:
        return 0
    threshold = int(math.ceil(total * ratio))
    return max(1, threshold)


def _find_first_no_projection_param(
    eval_visible_count,
    *,
    hi: float,
    n_steps: int = 320,
    visible_count_threshold: int = 0,
) -> Optional[float]:
    """Dense-sample a trajectory and return first param where visible count is low."""
    if hi <= 0:
        return None

    threshold = max(0, int(visible_count_threshold))
    c0 = int(eval_visible_count(0.0))
    if c0 <= threshold:
        return 0.0

    for i in range(1, n_steps + 1):
        t = hi * float(i) / float(n_steps)
        c = int(eval_visible_count(t))
        if c <= threshold:
            return float(t)
    return None


def _solve_symmetric_exit_limits(
    *,
    points_local: np.ndarray,
    intrinsics: np.ndarray,
    image_width: int,
    image_height: int,
    nav_radius: float,
    max_yaw: float = 85.0,
    visible_count_threshold: int = 0,
) -> Dict[str, Optional[float]]:
    """Solve left/right exit limits by foreground no-projection criterion."""
    W = max(1, int(image_width))
    H = max(1, int(image_height))
    pts = np.asarray(points_local, dtype=np.float32)
    if pts.ndim != 2 or pts.shape[1] != 3 or len(pts) == 0:
        return {
            "d_left": None, "d_right": None, "d_max": None,
            "yaw_left": None, "yaw_right": None, "yaw_max": None,
        }

    max_d = max(0.6, 2.8 * float(nav_radius))

    d_right = _find_first_no_projection_param(
        lambda d: _count_visible_fg_projection_points(
            points_local=pts, intrinsics=intrinsics,
            image_width=W, image_height=H,
            position_local=np.array([float(d), 0.0, 0.0], dtype=np.float32),
            yaw_deg=0.0,
        ),
        hi=max_d, visible_count_threshold=visible_count_threshold,
    )
    d_left = _find_first_no_projection_param(
        lambda d: _count_visible_fg_projection_points(
            points_local=pts, intrinsics=intrinsics,
            image_width=W, image_height=H,
            position_local=np.array([-float(d), 0.0, 0.0], dtype=np.float32),
            yaw_deg=0.0,
        ),
        hi=max_d, visible_count_threshold=visible_count_threshold,
    )

    yaw_right = _find_first_no_projection_param(
        lambda y: _count_visible_fg_projection_points(
            points_local=pts, intrinsics=intrinsics,
            image_width=W, image_height=H,
            position_local=np.zeros(3, dtype=np.float32),
            yaw_deg=float(y),
        ),
        hi=float(max_yaw), visible_count_threshold=visible_count_threshold,
    )
    yaw_left = _find_first_no_projection_param(
        lambda y: _count_visible_fg_projection_points(
            points_local=pts, intrinsics=intrinsics,
            image_width=W, image_height=H,
            position_local=np.zeros(3, dtype=np.float32),
            yaw_deg=-float(y),
        ),
        hi=float(max_yaw), visible_count_threshold=visible_count_threshold,
    )

    d_candidates = [float(v) for v in (d_left, d_right) if v is not None and v > 1e-5]
    yaw_candidates = [float(v) for v in (yaw_left, yaw_right) if v is not None and v > 1e-5]
    d_max = max(d_candidates) if d_candidates else None
    yaw_max_mag = max(yaw_candidates) if yaw_candidates else None

    return {
        "d_left": float(d_left) if d_left is not None else None,
        "d_right": float(d_right) if d_right is not None else None,
        "d_max": float(d_max) if d_max is not None else None,
        "yaw_left": float(yaw_left) if yaw_left is not None else None,
        "yaw_right": float(yaw_right) if yaw_right is not None else None,
        "yaw_max": float(yaw_max_mag) if yaw_max_mag is not None else None,
    }


# ---------------------------------------------------------------------------
# Round plan & interpolation
# ---------------------------------------------------------------------------

def _build_round_plan(
    combo: ComboDef,
    location_pool: Dict[str, List[float]],
    location_pose_pool: Dict[str, Dict],
    event_slots: Dict[str, Dict],
) -> Dict[str, Dict]:
    """Build round plan from combo definition."""
    actions = combo.actions
    targets = combo.target_sequence
    if len(actions) != len(targets):
        raise ValueError(
            f"Combo {combo.combo_id}: actions ({len(actions)}) and "
            f"targets ({len(targets)}) mismatch"
        )
    plan: Dict[str, Dict] = {}
    for i in range(combo.num_rounds):
        loc_to = targets[i]
        loc_from = "S" if i == 0 else targets[i - 1]
        event = actions[i].get("event", "")
        action = actions[i]["action"]

        if event and event in event_slots:
            look_at = event_slots[event]["position"]
        elif loc_to in event_slots:
            look_at = event_slots[loc_to]["position"]
        else:
            look_at = [0.0, 0.0, 0.0]

        plan[str(i)] = {
            "action": action,
            "event": event,
            "location_from": loc_from,
            "location_to": loc_to,
            "camera_start": location_pool.get(loc_from, [0.0, 0.0, 0.0]),
            "camera_end": location_pool.get(loc_to, [0.0, 0.0, 0.0]),
            "pose_start_c2w": location_pose_pool.get(loc_from, {}).get(
                "c2w", np.eye(4).tolist()
            ),
            "pose_end_c2w": location_pose_pool.get(loc_to, {}).get(
                "c2w", np.eye(4).tolist()
            ),
            "look_at": look_at,
        }
    return plan


def _interp_rotation(R0: np.ndarray, R1: np.ndarray, t: float) -> np.ndarray:
    R = (1.0 - t) * R0 + t * R1
    u, _, vt = np.linalg.svd(R)
    return (u @ vt).astype(np.float32)


def _interpolate_pose_sequence(
    round_plan: Dict[str, Dict],
    location_pose_pool: Dict[str, Dict],
    frames_per_round: int,
    first_c2w: np.ndarray,
    motion_primitive: str = "default",
    nav_radius: float = 1.0,
    seed: int = 42,
    calibration_points_local: Optional[np.ndarray] = None,
    calibration_intrinsics: Optional[np.ndarray] = None,
    calibration_image_width: Optional[int] = None,
    calibration_image_height: Optional[int] = None,
    exit_visible_ratio: float = 0.0,
) -> List[Dict]:
    """Interpolate per-frame poses and transform local → world.

    Supports two motion primitives:
      - case1_right / case1_left: Pure X translation (90% fg exit), back-and-forth
      - case2_right / case2_left: Triangular A→B + yaw at B → B→C + yaw at C
    """
    if frames_per_round < 2:
        frames_per_round = 2
    keys = sorted(round_plan.keys(), key=lambda x: int(x))

    # --- Case 1: 纯左右平移，去回去回 (90%像素出屏幕) ---
    if motion_primitive.startswith("case1_"):
        if len(keys) != 4:
            raise ValueError(f"case1 motion primitive expects 4 rounds, got {len(keys)}")

        sign = -1.0 if motion_primitive == "case1_left" else 1.0
        base_d = float(0.8 * nav_radius)
        exit_d = base_d  # fallback

        calib_pts = calibration_points_local
        calib_K = calibration_intrinsics
        calib_W = calibration_image_width
        calib_H = calibration_image_height

        if (
            calib_pts is not None and len(calib_pts) > 0
            and calib_K is not None and np.asarray(calib_K).shape == (3, 3)
            and calib_W is not None and calib_H is not None
        ):
            W_cal = max(1, int(calib_W))
            H_cal = max(1, int(calib_H))
            visible_threshold = _compute_exit_visible_threshold(calib_pts, 0.1)
            full_exit = _find_first_no_projection_param(
                lambda a: _count_visible_fg_projection_points(
                    points_local=np.asarray(calib_pts, dtype=np.float32),
                    intrinsics=np.asarray(calib_K, dtype=np.float32),
                    image_width=W_cal,
                    image_height=H_cal,
                    position_local=np.array(
                        [sign * base_d * float(a), 0.0, 0.0], dtype=np.float32,
                    ),
                    yaw_deg=0.0,
                ),
                hi=4.0,
                n_steps=640,
                visible_count_threshold=visible_threshold,
            )
            if full_exit is not None and full_exit > 1e-5:
                exit_d = base_d * float(full_exit)

        trans_dist = exit_d

        frames: List[Dict] = []
        frame_idx = 0
        for ri, rk in enumerate(keys):
            r = round_plan[rk]
            for si in range(frames_per_round):
                t = float(si) / max(1, frames_per_round - 1)
                if ri in (0, 2):
                    x = sign * trans_dist * t
                elif ri in (1, 3):
                    x = sign * trans_dist * (1.0 - t)
                else:
                    x = 0.0
                pos = np.array([x, 0.0, 0.0], dtype=np.float32)
                local = _yaw_c2w_local(0.0, position=pos)
                world = _local_to_world(local, first_c2w)
                frames.append({
                    "frame_idx": int(frame_idx),
                    "round_idx": int(rk),
                    "interp_t": float(t),
                    "location_from": r["location_from"],
                    "location_to": r["location_to"],
                    "action": r["action"],
                    "event": r["event"],
                    "c2w": world.astype(float).tolist(),
                })
                frame_idx += 1

        return frames

    # --- Case 2: Triangular layout  A -> B -> (yaw at B) -> C -> (yaw at C)
    if motion_primitive.startswith("case2_"):
        base_d = float(0.8 * nav_radius)
        base_Z = base_d / math.sqrt(3.0)
        center_pt = np.array([0.0, 0.0, 1.0], dtype=np.float32)
        if motion_primitive == "case2_right":
            sign = 1.0
        else:
            sign = -1.0

        pos_A = np.zeros(3, dtype=np.float32)
        dir_AB = np.array([sign * base_d, 0.0, -base_Z], dtype=np.float32)
        pos_B = dir_AB.copy()
        dir_BC_unit = np.array([-sign, 0.0, 0.0], dtype=np.float32)

        def _yaw_to_center(position_local: np.ndarray) -> float:
            rel = center_pt - np.asarray(position_local, dtype=np.float32)
            rel_z = float(rel[2])
            if abs(rel_z) < 1e-4:
                rel_z = 1e-4 if rel_z >= 0 else -1e-4
            yaw = math.degrees(math.atan2(float(rel[0]), rel_z))
            return float(np.clip(yaw, -85.0, 85.0))

        yaw_B = _yaw_to_center(pos_B)
        pos_C = np.array([0.0, 0.0, -base_Z], dtype=np.float32)
        yaw_C = _yaw_to_center(pos_C)

        calib_points = calibration_points_local
        if (
            calib_points is not None
            and len(calib_points) > 0
            and calibration_intrinsics is not None
            and np.asarray(calibration_intrinsics).shape == (3, 3)
            and calibration_image_width is not None
            and calibration_image_height is not None
        ):
            W_cal = max(1, int(calibration_image_width))
            H_cal = max(1, int(calibration_image_height))
            visible_threshold = _compute_exit_visible_threshold(
                calib_points, exit_visible_ratio,
            )
            target_exit_amp = 0.75
            _calib_pts = np.asarray(calib_points, dtype=np.float32)
            _calib_K = np.asarray(calibration_intrinsics, dtype=np.float32)

            # Step 1: Calibrate R0 (A -> B, yaw=0)
            r0_exit_scale = _find_first_no_projection_param(
                lambda a: _count_visible_fg_projection_points(
                    points_local=_calib_pts,
                    intrinsics=_calib_K,
                    image_width=W_cal,
                    image_height=H_cal,
                    position_local=(float(a) * dir_AB).astype(np.float32),
                    yaw_deg=0.0,
                ),
                hi=2.0,
                n_steps=640,
                visible_count_threshold=visible_threshold,
            )
            if r0_exit_scale is not None and r0_exit_scale > 1e-5:
                scale_AB = float(r0_exit_scale) / target_exit_amp
            else:
                scale_AB = 1.0

            pos_B = (scale_AB * dir_AB).astype(np.float32)

            # Step 2: Compute yaw_B
            center_pt = np.median(_calib_pts, axis=0).astype(np.float32)
            yaw_B = _yaw_to_center(pos_B)

            # Step 3: Calibrate R2 (B -> C, yaw=yaw_B)
            r2_exit_scale = _find_first_no_projection_param(
                lambda a: _count_visible_fg_projection_points(
                    points_local=_calib_pts,
                    intrinsics=_calib_K,
                    image_width=W_cal,
                    image_height=H_cal,
                    position_local=(pos_B + float(a) * base_d * dir_BC_unit).astype(np.float32),
                    yaw_deg=yaw_B,
                ),
                hi=2.0,
                n_steps=640,
                visible_count_threshold=visible_threshold,
            )
            if r2_exit_scale is not None and r2_exit_scale > 1e-5:
                scale_BC = float(r2_exit_scale) / target_exit_amp
            else:
                scale_BC = scale_AB

            dist_BC = base_d * scale_BC
            pos_C = (pos_B + dist_BC * dir_BC_unit).astype(np.float32)

            # Step 4: Compute yaw_C
            yaw_C = _yaw_to_center(pos_C)

        if len(keys) != 4:
            raise ValueError(
                f"case2 motion primitive expects 4 rounds, got {len(keys)}"
            )

        frames: List[Dict] = []
        frame_idx = 0
        for ri, rk in enumerate(keys):
            r = round_plan[rk]
            if ri == 0:
                pos_start = pos_A
                pos_end = pos_B
                yaw_start = 0.0
                yaw_end = 0.0
            elif ri == 1:
                pos_start = pos_B
                pos_end = pos_B
                yaw_start = 0.0
                yaw_end = yaw_B
            elif ri == 2:
                pos_start = pos_B
                pos_end = pos_C
                yaw_start = yaw_B
                yaw_end = yaw_B
            else:
                pos_start = pos_C
                pos_end = pos_C
                yaw_start = yaw_B
                yaw_end = yaw_C
            for si in range(frames_per_round):
                t = float(si) / max(1, frames_per_round - 1)
                pos = (1.0 - t) * pos_start + t * pos_end
                yaw = (1.0 - t) * yaw_start + t * yaw_end
                local = _yaw_c2w_local(yaw, position=pos)
                world = _local_to_world(local, first_c2w)
                frames.append({
                    "frame_idx": int(frame_idx),
                    "round_idx": int(rk),
                    "interp_t": float(t),
                    "location_from": r["location_from"],
                    "location_to": r["location_to"],
                    "action": r["action"],
                    "event": r["event"],
                    "c2w": world.astype(float).tolist(),
                })
                frame_idx += 1
        return frames

    # --- Default: waypoint lerp + slerp ---
    frames: List[Dict] = []
    frame_idx = 0
    for rk in keys:
        r = round_plan[rk]
        c0 = np.asarray(
            location_pose_pool[r["location_from"]]["c2w"], dtype=np.float32,
        )
        c1 = np.asarray(
            location_pose_pool[r["location_to"]]["c2w"], dtype=np.float32,
        )
        for s in range(frames_per_round):
            t = float(s) / float(frames_per_round)
            p = (1.0 - t) * c0[:3, 3] + t * c1[:3, 3]
            R = _interp_rotation(c0[:3, :3], c1[:3, :3], t)
            local = np.eye(4, dtype=np.float32)
            local[:3, :3] = R
            local[:3, 3] = p
            world = _local_to_world(local, first_c2w)
            frames.append({
                "frame_idx": int(frame_idx),
                "round_idx": int(rk),
                "interp_t": float(t),
                "location_from": r["location_from"],
                "location_to": r["location_to"],
                "action": r["action"],
                "event": r["event"],
                "c2w": world.astype(float).tolist(),
            })
            frame_idx += 1
    last = round_plan[keys[-1]]
    c_end = np.asarray(
        location_pose_pool[last["location_to"]]["c2w"], dtype=np.float32,
    )
    world_end = _local_to_world(c_end, first_c2w)
    frames.append({
        "frame_idx": int(frame_idx),
        "round_idx": int(keys[-1]),
        "interp_t": 1.0,
        "location_from": last["location_to"],
        "location_to": last["location_to"],
        "action": "terminal",
        "event": last["event"],
        "c2w": world_end.astype(float).tolist(),
    })
    return frames


