def _load_image_rgb(image_path: str) -> np.ndarray:
    img_bgr = cv2.imread(image_path, cv2.IMREAD_COLOR)
    if img_bgr is None:
        raise FileNotFoundError(f"Cannot load image: {image_path}")
    return cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB)


def _prepare_image_to_target(image_rgb: np.ndarray, target_h: int, target_w: int) -> np.ndarray:
    """Resize/crop image to exact target size with same policy as scene builders."""
    from liveworld.geometry_utils import resize_short_edge_and_center_crop

    try:
        return resize_short_edge_and_center_crop(image_rgb, target_h, target_w)
    except ValueError:
        pass

    h, w = image_rgb.shape[:2]
    scale = min(target_w / float(w), target_h / float(h))
    new_w = max(1, int(round(w * scale)))
    new_h = max(1, int(round(h * scale)))
    resized = cv2.resize(image_rgb, (new_w, new_h), interpolation=cv2.INTER_LINEAR)
    canvas = np.zeros((target_h, target_w, 3), dtype=np.uint8)
    y0 = (target_h - new_h) // 2
    x0 = (target_w - new_w) // 2
    canvas[y0 : y0 + new_h, x0 : x0 + new_w] = resized
    return canvas


def _prepare_detection_image_for_scene(
    image_path: str,
    scene_meta: Optional[Dict],
) -> str:
    """Return detection image path aligned to scene_meta target geometry."""
    if scene_meta is None:
        return image_path
    target_h = scene_meta.get("target_h")
    target_w = scene_meta.get("target_w")
    if target_h is None or target_w is None:
        return image_path
    target_h = int(target_h)
    target_w = int(target_w)
    if target_h <= 0 or target_w <= 0:
        return image_path

    raw_rgb = _load_image_rgb(image_path)
    if raw_rgb.shape[0] == target_h and raw_rgb.shape[1] == target_w:
        return image_path

    aligned_rgb = _prepare_image_to_target(raw_rgb, target_h, target_w)
    src = Path(image_path).resolve()
    cache_dir = Path(".cache/event_bench/detect_aligned")
    cache_dir.mkdir(parents=True, exist_ok=True)
    key = f"{src}|{target_h}x{target_w}"
    digest = hashlib.sha1(key.encode("utf-8")).hexdigest()[:16]
    out_path = cache_dir / f"{src.stem}_{target_h}x{target_w}_{digest}.png"
    if not out_path.exists():
        cv2.imwrite(str(out_path), cv2.cvtColor(aligned_rgb, cv2.COLOR_RGB2BGR))
    return str(out_path)


def extract_scene_features(image_rgb: np.ndarray) -> SceneFeatures:
    gray = cv2.cvtColor(image_rgb, cv2.COLOR_RGB2GRAY)
    brightness_mean = float(np.mean(gray) / 255.0)
    edges = cv2.Canny(gray, threshold1=80, threshold2=160)
    edge_density = float(np.mean(edges > 0))
    texture_std = float(np.std(cv2.Laplacian(gray, cv2.CV_32F)))
    texture_norm = float(np.clip(texture_std / 40.0, 0.0, 1.0))
    clutter_score = float(np.clip(0.6 * edge_density + 0.4 * texture_norm, 0.0, 1.0))

    if clutter_score > 0.42:
        environment_hint = "cluttered"
    elif clutter_score > 0.22:
        environment_hint = "moderate"
    else:
        environment_hint = "open"

    return SceneFeatures(
        brightness_mean=brightness_mean,
        edge_density=edge_density,
        texture_std=texture_std,
        clutter_score=clutter_score,
        environment_hint=environment_hint,
    )


