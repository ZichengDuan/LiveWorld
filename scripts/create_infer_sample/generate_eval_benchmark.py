@dataclass
class ComboDef:
    combo_id: str
    case_type: str               # "case1" or "case2"
    num_events: int              # 1, 2, or 3
    num_rounds: int              # 2-6
    first_frame_has_event: bool
    actions: List[Dict[str, str]]      # per-round {"action": ..., "event": ...}
    target_sequence: List[str]         # per-round target location ID
    motion_primitive: str              # "case1" | "case2_right" | "case2_left"
    is_core: bool = True               # core = generated by default
    event_ids: List[str] = field(default_factory=list)  # which event IDs are used
    frames_per_round: Optional[int] = None   # override CLI --frames-per-round
    fps: int = 16                            # video fps (metadata only)
    layout_scale: float = 1.0                # scale factor for layout radius


@dataclass
class LayoutConfig:
    """Spatial layout configuration for events and camera positions."""
    name: str                                # "triangle", "square", "line", etc.
    event_positions: Dict[str, List[float]]  # event_id -> [x, y, z] local
    centroid: np.ndarray                     # shape (3,), layout centroid
    camera_orientation: str                  # "outward" | "inward" | "forward"
    interpolation: str                       # "lerp_slerp"
    path_noise_std: float                    # 0.0 = no noise


def _make_combo(combo_id, case_type, num_events, num_rounds, first_frame_has_event,
                actions, targets, motion_primitive="default", is_core=True,
                event_ids=None):
    if event_ids is None:
        event_ids = ["A", "B", "C"][:num_events]
    return ComboDef(
        combo_id=combo_id, case_type=case_type, num_events=num_events,
        num_rounds=num_rounds, first_frame_has_event=first_frame_has_event,
        actions=actions, target_sequence=targets,
        motion_primitive=motion_primitive, is_core=is_core,
        event_ids=event_ids,
    )


